 ;Bubble sort algoritmasinda icdongu siralama isinin yapildigi yerdir. Her seferinde en bastan en sona giderek ikili kiyaslamaya gore sayilarin yer degismesini saglar.
icdongu:
 MOV DX, [BX + SI]     ; ikili kiyaslama yapilacagi zaman soldaki 16bitlik sayiyi tutar.
 MOV AX, [BX + SI + 2] ; ikili kiyaslama yapilacagi zaman sagdaki 16bitlik sayiyi tutar. (soldaki sayidan sagdakine gecisin 2byte olmasina dikkat!)
 
 CMP AX, DX ; emu8086'daki kiyaslama yapmayi saglayan opcode. kiyaslama sonucuna gore CF flagini 1(DX buyukse) veya 0(AX buyuk esitse) yapar.
 JAE @BUYUK_ESITSE    ; eger AX >= DX (cf=0) ise BUYUKESIT bloguna atlar(swap yapmaya gerek yok)
 JB @KUCUKSE   ; eger AX < DX (cf=1) ise KUCUKSE bloguna atlar(swap yapilacak)
        
    ;swap olmayacagi icin registerdaki sayilari eski yerlerine movlayacagimiz blok
    @BUYUK_ESITSE:           
    MOV [BX + SI], DX     ; AX >= DX ise swape gerek yok. DX registerini tekrar eski yerine movlayabiliriz.
    MOV [BX + SI + 2], AX ; yukaridaki gibi swap olmayacagi icin AX registerini eski yerine movluyoruz.
    ADD SI, 2             ; siradaki ikili kiyaslama icin icdongu degiskenini artiriyoruz. +2 cunku 2byte-2byte ilerlemeler yapacagiz.

    CMP SI, doubled_dizi_boyutu ; mevcut dongude tum elemanlar icin ikili kiyaslama yapildi mi diye kontrol ediyoruz. ZF=0(farkli) veya ZF=1(esit) flaglerine gore atlama yapilacak. 
    JNZ icdongu ; (ZF=0) mevcut dongude halen daha ikili kiyaslama yapilmayan elemanlar var. onlarin kontrolu icin icdonguye devam etmemiz lazim.
    LOOP disdongu ; (ZF=1) mevcut dongude ikili kiyaslamalar tamamlandi. siradaki dizi indexine gecmek icin disdonguye atliyoruz.
        
    ;swapleme islemini yapacagimiz blok
    @KUCUKSE:           
    MOV CX, [BX + SI]     ; soldaki sayiyi(buyuk olan) kaybetmemek icin ucuncu bir 16bitlik gecici registera movluyoruz. 
    MOV [BX + SI], AX     ; sagdaki sayi daha kucuk oldugu icin soldaki sayinin yerine movluyoruz.(kucukten buyuge siralama yapiyoruz)
    MOV [BX + SI + 2], CX ; buyuk olan sayiyi eskiden sagda olan sayinin yerine movluyoruz.
    ADD SI, 2             ; siradaki ikili kiyaslama icin icdongu degiskenini artiriyoruz. +2 cunku 2byte-2byte ilerlemeler yapacagiz.
    
    CMP SI, doubled_dizi_boyutu ; mevcut dongude tum elemanlar icin ikili kiyaslama yapildi mi diye kontrol ediyoruz. ZF=0(farkli) veya ZF=1(esit) flaglerine gore atlama yapilacak.
    JNZ icdongu ; (ZF=0) (ZF=0) mevcut dongude halen daha ikili kiyaslama yapilmayan elemanlar var. onlarin kontrolu icin icdonguye devam etmemiz lazim.       
    LOOP disdongu ; (ZF=1) mevcut dongude ikili kiyaslamalar tamamlandi. siradaki dizi indexine gecmek icin disdonguye atliyoruz.  

